CREATE TABLE ANALYTICS (
    auto_id BIGINT IDENTITY(1,1) NOT NULL PRIMARY KEY,
    event_id BIGINT,
    type VARCHAR(255),
    session_id VARCHAR(255),
    label VARCHAR(255),
    value VARCHAR(255),
    action VARCHAR(255),
    analytic_ts timestamp without time zone
);

GRANT SELECT ON ANALYTICS TO GROUP READ_ONLY;
GRANT ALL ON ANALYTICS TO GROUP READ_WRITE;
GRANT ALL ON ANALYTICS TO GROUP SCHEMA_MANAGER;
GRANT USAGE, SELECT ON SEQUENCE analytics_auto_id_seq TO GROUP READONLY;
GRANT USAGE, SELECT ON SEQUENCE analytics_auto_id_seq TO GROUP SCHEMA_MANAGER;
GRANT USAGE, SELECT ON SEQUENCE analytics_auto_id_seq TO GROUP READ_WRITE;
