CREATE TABLE PROTECT.CUSTOMATTRIBUTEDEFINITION_LOG
AS
   SELECT *
     FROM PROTECT.CUSTOMATTRIBUTEDEFINITION
    WHERE 1 = 2;

ALTER TABLE PROTECT.CUSTOMATTRIBUTEDEFINITION_LOG ADD (LOGTIME TIMESTAMP(6));

CREATE OR REPLACE TRIGGER PROTECT.CUSTOMATTRDEF_UPDATE
   AFTER UPDATE OF NAME, COLUMNINDEX, DISPLAYORDER, ISEMAILADDRESS
   ON PROTECT.CUSTOMATTRIBUTEDEFINITION
   REFERENCING NEW AS NEW OLD AS OLD
   FOR EACH ROW
DECLARE
BEGIN
   INSERT INTO PROTECT.CUSTOMATTRIBUTEDEFINITION_LOG
      (SELECT :NEW.CUSTOMATTRIBUTEDEFINITIONID,
              :NEW.NAME,
              :NEW.COLUMNINDEX,
              :NEW.DISPLAYORDER,
              :NEW.ISEMAILADDRESS,
              CURRENT_TIMESTAMP
         FROM DUAL);
EXCEPTION
   WHEN OTHERS
   THEN
      NULL;
END CUSTOMATTRDEF_UPDATE;