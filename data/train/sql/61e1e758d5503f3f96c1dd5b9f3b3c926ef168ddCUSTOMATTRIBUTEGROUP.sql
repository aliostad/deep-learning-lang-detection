CREATE TABLE PROTECT.CUSTOMATTRIBUTEGROUP_LOG
AS
   SELECT *
     FROM PROTECT.CUSTOMATTRIBUTEGROUP
    WHERE 1 = 2;

ALTER TABLE PROTECT.CUSTOMATTRIBUTEGROUP_LOG ADD (LOGTIME TIMESTAMP(6));

CREATE OR REPLACE TRIGGER PROTECT.CUSTOMATTRIBUTEGROUP_UPDATE
   AFTER UPDATE OF NAME, ISINTERNATIONALIZED
   ON PROTECT.CUSTOMATTRIBUTEGROUP
   REFERENCING NEW AS NEW OLD AS OLD
   FOR EACH ROW
DECLARE
BEGIN
   INSERT INTO PROTECT.CUSTOMATTRIBUTEGROUP_LOG
      (SELECT :NEW.CUSTOMATTRIBUTEGROUPID,
              :NEW.NAME,
              :NEW.ISINTERNATIONALIZED,
              CURRENT_TIMESTAMP
         FROM DUAL);
EXCEPTION
   WHEN OTHERS
   THEN
      NULL;
END CUSTOMATTRIBUTEGROUP_UPDATE;