CREATE OR REPLACE VIEW SENSORS AS
SELECT 
SM.MODEL,S.SEQ,S.DESCRIPTION,SM.DESCRIPTION MODEL_DESCRIPTION,S.DATE_INSTALLED,S.DATE_REMOVED,S.CABINET,S.STATION_FT,S.OFFSET_FT,S.SENSOR_DEPTH_IN,S.ORIENTATION,S.COLLECTION_TYPE,S.DYNAMIC_STATIC,S.ID,S.LAYER_ID,SENSOR_ID
,SM.ID SENSOR_MODEL_ID,SM.MEASUREMENT_TYPE,SM.DATA_VALUES_TABLE,SM.MODEL_NUM,SM.MEASUREMENT_UNITS,SM.MIN_POSSIBLE_VALUE,SM.MAX_POSSIBLE_VALUE,SM.MIN_EXPECTED_VALUE,SM.MAX_EXPECTED_VALUE
FROM SENSOR S JOIN SENSOR_MODEL SM ON S.SENSOR_MODEL_ID = SM.ID 

