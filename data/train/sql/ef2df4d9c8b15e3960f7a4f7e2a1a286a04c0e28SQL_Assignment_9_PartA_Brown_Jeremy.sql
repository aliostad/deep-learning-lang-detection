--PART A #1 Union Operation
SELECT CUSTOMER_NUM, CUSTOMER_NAME, REP_NUM
FROM CUSTOMER
WHERE REP_NUM = 65
UNION
SELECT CUSTOMER.CUSTOMER_NUM, CUSTOMER_NAME, REP_NUM
FROM CUSTOMER, ORDERS
WHERE ORDERS.CUSTOMER_NUM = CUSTOMER.CUSTOMER_NUM;

--PART A #2 Intersect Operation
SELECT CUSTOMER_NUM, CUSTOMER_NAME, REP_NUM
FROM CUSTOMER
WHERE REP_NUM = 65
INTERSECT
SELECT CUSTOMER.CUSTOMER_NUM, CUSTOMER_NAME, REP_NUM
FROM CUSTOMER, ORDERS
WHERE ORDERS.CUSTOMER_NUM = CUSTOMER.CUSTOMER_NUM;

--PART A #3 Except Operation
SELECT CUSTOMER_NUM, CUSTOMER_NAME, REP_NUM
FROM CUSTOMER
WHERE REP_NUM = 65
EXCEPT
SELECT CUSTOMER.CUSTOMER_NUM, CUSTOMER_NAME, REP_NUM
FROM CUSTOMER, ORDERS
WHERE ORDERS.CUSTOMER_NUM = CUSTOMER.CUSTOMER_NUM
