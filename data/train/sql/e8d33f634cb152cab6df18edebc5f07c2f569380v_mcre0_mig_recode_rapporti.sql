CREATE OR REPLACE FORCE VIEW MCRE_OWN.V_MCRE0_MIG_RECODE_RAPPORTI
(
   COD_ABI_OLD,
   COD_NDG_OLD,
   COD_RAPPORTO_OLD,
   COD_ABI_NEW,
   COD_NDG_NEW,
   COD_RAPPORTO_NEW
)
AS
   SELECT S.COD_ABI AS COD_ABI_OLD,
          RN.COD_NDG_OLD,
          RN.COD_RAPPORTO_OLD,
          T.COD_ABI AS COD_ABI_NEW,
          RN.COD_NDG_NEW,
          RN.COD_RAPPORTO_NEW
     FROM (SELECT SUBSTR (COD_CONTESTO_BANCA_OLD, 2, 3) AS COD_ISTITUTO_OLD,
                  SUBSTR (COD_CONTESTO_BANCA_NEW, 2, 3) AS COD_ISTITUTO_NEW,
                  COD_NDG_OLD,
                  COD_RAPPORTO_OLD,
                  COD_NDG_NEW,
                  COD_RAPPORTO_NEW
             FROM TE_MCRE0_MIG_RECODE_RAPPORTI) RN,
          T_MCRE0_APP_ISTITUTI S,
          T_MCRE0_APP_ISTITUTI T
    WHERE     RN.COD_ISTITUTO_OLD = S.COD_ISTITUTO(+)
          AND RN.COD_ISTITUTO_NEW = T.COD_ISTITUTO(+)
--446.992
;
