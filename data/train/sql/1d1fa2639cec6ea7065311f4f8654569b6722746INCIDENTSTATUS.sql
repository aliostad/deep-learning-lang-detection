CREATE TABLE PROTECT.INCIDENTSTATUS_LOG
AS
   SELECT *
     FROM PROTECT.INCIDENTSTATUS
    WHERE 1 = 2;

ALTER TABLE PROTECT.INCIDENTSTATUS_LOG ADD (LOGTIME TIMESTAMP(6));

CREATE OR REPLACE TRIGGER PROTECT.INCIDENTSTATUS_UPDATE
   AFTER UPDATE OF NAME, ISDELETED, MENUORDER, ISDEFAULT, ISINTERNATIONALIZED
   ON PROTECT.INCIDENTSTATUS
   REFERENCING NEW AS NEW OLD AS OLD
   FOR EACH ROW
DECLARE
BEGIN
   INSERT INTO PROTECT.INCIDENTSTATUS_LOG
      (SELECT :NEW.INCIDENTSTATUSID,
              :NEW.NAME,
              :NEW.MENUORDER,
              :NEW.ISDEFAULT,
              :NEW.ISDELETED,
              :NEW.ISINTERNATIONALIZED,
              CURRENT_TIMESTAMP
         FROM DUAL);
EXCEPTION
   WHEN OTHERS
   THEN
      NULL;
END INCIDENTSTATUS_UPDATE;