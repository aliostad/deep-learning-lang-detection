
  CREATE OR REPLACE  VIEW HV_EQUITY_TOP10 (SCHEME, NAV_DATE, SECURITY_NAME, SECURITY, INVEST_TYPE, UNITS, AMOUNT, ASSET_TYPE, PUR_VALUE) AS 
  SELECT SCHNAVBD.SCHEME,
  SCHNAVBD.NAV_DATE,
  --scheme.scheme_name,
  security.name security_name,
  SCHNAVBD.SECURITY,
  SCHNAVBD.INVEST_TYPE,
  SCHNAVBD.UNITS,
  -SCHNAVBD.AMOUNT amount,
  SCHNAVBD.ASSET_TYPE,
  SCHNAVBD.PUR_VALUE
FROM HI_SCHNAVBD_CURR SCHNAVBD,hm_security security
WHERE schnavbd.SECURITY=security.security(+)  and
(SCHNAVBD.NAV_DATE >'31 mar 2013')
AND (SCHNAVBD.INVEST_TYPE='INVEST');
 
