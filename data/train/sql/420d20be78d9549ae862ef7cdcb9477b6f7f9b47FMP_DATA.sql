
  CREATE OR REPLACE VIEW FMP_DATA (SCHEME, START_DATE, END_DATE, NAV, SCHCLASS, ASSET_MGMT) AS 
  SELECT HSC.SCHEME,
  HSC.START_DATE,
  HSC.END_DATE,
  HWC.NAV,
  HWC.SCHCLASS,
  AUM.ASSET_MGMT
 FROM hm_scheme HSC,
  HI_WEEKNAV_CURR HWC,(select (  SUM(LAST_NET_ASSETS)/10000000 )asset_mgmt FROM hm_scheme HSC,
  HI_WEEKNAV_CURR HWC
WHERE HSC.scheme=HWC.SCHEME AND
  HSC.RECTYPE       ='L'
AND HSC.ACTIVATE_Y_N='Y'
AND HSC.SCHEME_TYPE ='C'
  AND HWC.WEEKEND_DT='08 JAN 2014'
AND HWC.SCHCLASS<>'GLOBAL'
AND HWC.SCHEME   ='MAY370D113'
) aum
WHERE HSC.scheme=HWC.SCHEME
AND HSC.RECTYPE       ='L'
AND HSC.ACTIVATE_Y_N='Y'
AND HSC.SCHEME_TYPE ='C'
AND HWC.WEEKEND_DT='08 JAN 2014'
AND HWC.SCHCLASS<>'GLOBAL'
AND HWC.SCHEME   ='MAY370D113';
