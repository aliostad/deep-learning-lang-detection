package com.xtivia.xip.entity;

public class ServiceDefinition {
	private String serviceName;
	private String serviceType;
	private String serviceProvider;
	private String serviceAction;
	private String serviceText;
	private String serviceInputMappings;
	private String serviceOutputMappings;
	private String serviceRoles;
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
	public String getServiceType() {
		return serviceType;
	}
	public void setServiceType(String serviceType) {
		this.serviceType = serviceType;
	}
	public String getServiceProvider() {
		return serviceProvider;
	}
	public void setServiceProvider(String serviceProvider) {
		this.serviceProvider = serviceProvider;
	}
	public String getServiceAction() {
		return serviceAction;
	}
	public void setServiceAction(String serviceAction) {
		this.serviceAction = serviceAction;
	}
	public String getServiceText() {
		return serviceText;
	}
	public void setServiceText(String serviceText) {
		this.serviceText = serviceText;
	}
	public String getServiceInputMappings() {
		return serviceInputMappings;
	}
	public void setServiceInputMappings(String serviceInputMappings) {
		this.serviceInputMappings = serviceInputMappings;
	}
	public String getServiceOutputMappings() {
		return serviceOutputMappings;
	}
	public void setServiceOutputMappings(String serviceOutputMappings) {
		this.serviceOutputMappings = serviceOutputMappings;
	}
	public String getServiceRoles() {
		return serviceRoles;
	}
	public void setServiceRoles(String serviceRoles) {
		this.serviceRoles = serviceRoles;
	}
}
