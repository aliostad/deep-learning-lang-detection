#include "SocketStream.h"

namespace System
{
	namespace Net
	{
		///////////////////////////////////////////////////////////////////////
		SocketStream::SocketStream ( SocketStreamBuf* pStreamBuffer )
			: std::iostream (NULL)
		///////////////////////////////////////////////////////////////////////
		{
			m_pStreamBuffer = pStreamBuffer;

			init (m_pStreamBuffer);
		}

		///////////////////////////////////////////////////////////////////////
		SocketStream::SocketStream ( Socket *pSocket )
			: std::iostream (NULL)
		///////////////////////////////////////////////////////////////////////
		{
			m_pStreamBuffer = new SocketStreamBuf ( pSocket );

			init (m_pStreamBuffer);
		}

		///////////////////////////////////////////////////////////////////////
		SocketStream::~SocketStream ( void )
		///////////////////////////////////////////////////////////////////////
		{
			delete m_pStreamBuffer;
		}
	}
}
