#include "SaveLoad.h"

SaveLoad* SaveLoad::pSaveLoad = NULL;

//--------------------------------------------------------------------------------------
SaveLoad::SaveLoad()
//--------------------------------------------------------------------------------------
{
}


//--------------------------------------------------------------------------------------
SaveLoad::~SaveLoad()
//--------------------------------------------------------------------------------------
{
}


//--------------------------------------------------------------------------
SaveLoad* SaveLoad::GetInstance()
//--------------------------------------------------------------------------
{
	if(NULL == pSaveLoad)
	{
		pSaveLoad = new SaveLoad();
	}

	return pSaveLoad;
}

//--------------------------------------------------------------------------
void SaveLoad::FreeInstance()
//--------------------------------------------------------------------------
{
	if(NULL != pSaveLoad)
	{
		delete pSaveLoad;
		pSaveLoad = NULL;
	}
}

