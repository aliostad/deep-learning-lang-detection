CREATE TABLE PROTECT.INFORMATIONMONITOR_LOG
AS
   SELECT *
     FROM PROTECT.INFORMATIONMONITOR
    WHERE 1 = 2;

ALTER TABLE PROTECT.INFORMATIONMONITOR_LOG ADD (LOGTIME TIMESTAMP(6));

CREATE OR REPLACE TRIGGER PROTECT.INFORMATIONMONITOR_UPDATE
   AFTER UPDATE OF ISDELETED
   ON PROTECT.INFORMATIONMONITOR
   REFERENCING NEW AS NEW OLD AS OLD
   FOR EACH ROW
DECLARE
BEGIN
   INSERT INTO PROTECT.INFORMATIONMONITOR_LOG
      (SELECT :NEW.INFORMATIONMONITORID,
              :NEW.MONITORNAME,
              :NEW.UNIQUENAME,
              :NEW.HOST,
              :NEW.PORT,
              :NEW.ISDELETED,
              CURRENT_TIMESTAMP
         FROM DUAL);
EXCEPTION
   WHEN OTHERS
   THEN
      NULL;
END INFORMATIONMONITOR_UPDATE;