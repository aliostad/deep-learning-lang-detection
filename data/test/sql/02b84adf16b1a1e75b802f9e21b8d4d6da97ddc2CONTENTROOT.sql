CREATE TABLE PROTECT.CONTENTROOT_LOG
AS
   SELECT *
     FROM PROTECT.CONTENTROOT
    WHERE 1 = 2;

ALTER TABLE PROTECT.CONTENTROOT_LOG ADD (LOGTIME TIMESTAMP(6));

CREATE OR REPLACE TRIGGER PROTECT.CONTENTROOT_UPDATE
   AFTER UPDATE OF ISDELETED
   ON PROTECT.CONTENTROOT
   REFERENCING NEW AS NEW OLD AS OLD
   FOR EACH ROW
DECLARE
BEGIN
   INSERT INTO PROTECT.CONTENTROOT_LOG
      (SELECT :NEW.USERNAME,
              :NEW.PASSWORDID,
              :NEW.REMEDIATIONUSERNAME,
              :NEW.REMEDIATIONPASSWORDID,
              :NEW.ISDELETED,
              :NEW.CREDENTIALID,
              :NEW.REMEDIATIONCREDENTIALID,
              :NEW.CONTENTROOTDATAID,
              :NEW.CONTENTROOTID,
              :NEW.COURSEID,
              :NEW.URI,
              :NEW.MAXDEPTH,
              CURRENT_TIMESTAMP
         FROM DUAL);
EXCEPTION
   WHEN OTHERS
   THEN
      NULL;
END CONTENTROOT_UPDATE;