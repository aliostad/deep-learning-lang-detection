CREATE TABLE PROTECT.AGENTSTATUS_LOG
AS
   SELECT *
     FROM PROTECT.AGENTSTATUS
    WHERE 1 = 2;

ALTER TABLE PROTECT.AGENTSTATUS_LOG ADD (LOGTIME TIMESTAMP(6));

CREATE OR REPLACE TRIGGER PROTECT.AGENTSTATUS_UPDATE
   AFTER UPDATE OF STATUS
   ON PROTECT.AGENTSTATUS
   REFERENCING NEW AS NEW OLD AS OLD
   FOR EACH ROW
DECLARE
BEGIN
   INSERT INTO PROTECT.AGENTSTATUS_LOG
      (SELECT :NEW.AGENTSTATUSID,
              :NEW.AGENTID,
              :NEW.CATEGORYID,
              :NEW.STATUS,
              :NEW.CATEGORYSTATUSID,
              CURRENT_TIMESTAMP
         FROM DUAL);
EXCEPTION
   WHEN OTHERS
   THEN
      NULL;
END AGENTSTATUS_UPDATE;